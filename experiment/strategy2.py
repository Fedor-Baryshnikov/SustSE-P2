import httpx
import asyncio
import sys

OLLAMA_API_URL = "http://127.0.0.1:11434/api/chat"
OLLAMA_MM = "gemma3:12b"
OLLAMA_SM = "gemma3:4b"

PROMPT = """Write a complete Python script that uses FastAPI to create a REST API for a simple task management system. It should include:
1. A SQLite database using SQLAlchemy. 
2. Endpoints to Create, Read, Update, and Delete tasks. 
3. Task model should have fields: id, title, description, completed, and created_at. 
4. Use Pydantic for request and response validation. 
5. Include basic error handling for 404 cases.
Please provide the full, working code."""

MM_OUTLINE_PROMPT = """    You are the control LLM. 
Your task is to split the following code into up to 3 separate tasks.
Write each task as a clear, unambiguous instruction. 
Each task must be self-contained and should not require information from the other tasks to be completed.
Do keep in mind that you will have to reassemble the final answer from the 3 smaller parts, so try to split in a way that makes it easy to reassemble.
Do not provide any features that are not explicitly requested in the original user message.
Do not make up any functions without explaining how they work with code.
Split the tasks with a newline character. Do not add anything else in your response.
The code to split is:
The task is: """

SM_IMPLEMENT_PROMPT = """
You are a smaller LLM.
Your task is to generate a response for the given part of the code, based on the instructions provided.
In your response, do not include any explanations or reasoning, just provide the answer for the given part as a single JSON string, without any additional text.
The instructions are: """

SM_RECOMBINE_PROMPT = """
You are the control LLM.
Your task is to take the 3 smaller parts generated by the smaller LLMs and reassemble them into a single, coherent response to the original user message.
Return your answer as normal text and not a JSON string.
Do not include any statements relating to the fact that you are recombining parts, just provide the final answer as if you were responding to the original user message.
The 3 parts are: """


async def main():
    if len(sys.argv) < 2:
        print("Missing output prefix")
        return

    output_prefix = sys.argv[1]

    tokens_12b = 0
    tokens_4b = 0

    print(f"[STRATEGY 2] Step 1: Generating outline using {OLLAMA_MM}...")
    outline_payload = {
        "model": OLLAMA_MM,
        "messages": [{"role": "user", "content": MM_OUTLINE_PROMPT + PROMPT}],
        "stream": False
    }

    try:
        async with httpx.AsyncClient() as client:
            response = await client.post(OLLAMA_API_URL, json=outline_payload, timeout=120.0)
            response.raise_for_status()
            result = response.json()

            outline_content = result.get("message", {}).get("content", "")
            tokens_12b = result.get("eval_count", 0)

            outline_file = f"{output_prefix}_outline_12b.txt"
            with open(outline_file, "w") as f:
                f.write(outline_content)

            print(f"[STRATEGY 2] Outline saved to {outline_file}")

    except Exception as e:
        print(f"[STRATEGY 2] Error in outline: {e}")
        return

    tasks = [task for task in outline_content.split("\n") if task]
    SM_responses = []

    print(f"[STRATEGY 2] Step 2: Implementing using {OLLAMA_SM}...")
    for i, task in enumerate(tasks):
        sm_payload = {
            "model": OLLAMA_SM,
            "messages": [{"role": "user", "content": SM_IMPLEMENT_PROMPT + task}],
            "stream": False
        }
        try:
            async with httpx.AsyncClient() as client:
                response = await client.post(OLLAMA_API_URL, json=sm_payload, timeout=300.0)
                response.raise_for_status()
                result = response.json()
                sm_content = result.get("message", {}).get("content", "")
                SM_responses.append(sm_content)
                tokens_4b += result.get("eval_count", 0)
        except Exception as e:
            SM_responses.append(f"Error processing task: {str(e)}")

    final_code = "\n".join(SM_responses)
    code_file = f"{output_prefix}_final_4b.txt"
    with open(code_file, "w") as f:
        f.write(final_code)

    tokens_file = f"{output_prefix}_tokens.txt"
    total_tokens_strategy2 = tokens_12b + tokens_4b
    with open(tokens_file, "w") as f:
        f.write(f"{OLLAMA_MM}:{tokens_12b}\n{OLLAMA_SM}:{total_tokens_strategy2}\n")

    print(f"STRATEGY 2: Code saved to {code_file}")
    print(f"STRATEGY 2: Tokens ({OLLAMA_MM}): {tokens_12b}")
    print(f"STRATEGY 2: Total Tokens ({OLLAMA_SM}): {total_tokens_strategy2}")

    print(f"[STRATEGY 2] Step 3: Recombining using {OLLAMA_SM}...")
    recombine_payload = {
        "model": OLLAMA_SM,
        "messages": [{"role": "user", "content": SM_RECOMBINE_PROMPT + "\n".join(SM_responses)}],
        "stream": False
    }

    try:
        async with httpx.AsyncClient() as client:
            response = await client.post(OLLAMA_API_URL, json=recombine_payload, timeout=300.0)
            response.raise_for_status()
            result = response.json()
            recombined_output = result.get("message", {}).get("content", "")
            tokens_4b += result.get("eval_count", 0)

            recombined_file = f"{output_prefix}_recombined_4b.txt"
            with open(recombined_file, "w") as f:
                f.write(recombined_output)

            total_tokens_strategy2 = tokens_12b + tokens_4b
            with open(tokens_file, "w") as f:
                f.write(f"{OLLAMA_MM}:{tokens_12b}\n{OLLAMA_SM}:{total_tokens_strategy2}\n")

            print(f"STRATEGY 2: Recombined output saved to {recombined_file}")
            print(f"STRATEGY 2: Total Tokens ({OLLAMA_SM}): {total_tokens_strategy2}")

    except Exception as e:
        print(f"STRATEGY 2: Recombine Error: {e}")


if __name__ == "__main__":
    asyncio.run(main())